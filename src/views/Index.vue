import ghost from "@/assets/img/ghost.png";
import dancingMan from "@/assets/img/dancingman.gif";
import shield from "@/assets/img/shield.png";

import communityPage from "@/assets/img/AI_ART.png";
import feed from "@/assets/img/feed.png";
import profile from "@/assets/img/profile.png";

export default {
  data() {
    return {
      patternVue,
      community,
      communityPic,
      earth,
      social,
      ghost,
      dancingMan,
      shield,
      communityPage,
      profile,
      feed,
      typeValue: "",
      typeStatus: false,
      displayTextArray: ["World!", "Family!", "Friends!", "Grandma!"],
      typingSpeed: 100,
      erasingSpeed: 100,
      newTextDelay: 2000,
      displayTextArrayIndex: 0,
      charIndex: 0,
    };
  },
  components: {
    IndexNavbar,
    FooterComponent,
  },
  props: {},
  created() {
    setTimeout(this.typeText, this.newTextDelay + 200);
  },
  methods: {
    typeText() {
      if (this.charIndex < this.displayTextArray[this.displayTextArrayIndex].length) {
        if (!this.typeStatus) this.typeStatus = true;
        this.typeValue += this.displayTextArray[this.displayTextArrayIndex].charAt(
          this.charIndex
        );
        this.charIndex += 1;
        setTimeout(this.typeText, this.typingSpeed);
      } else {
        this.typeStatus = false;
        setTimeout(this.eraseText, this.newTextDelay);
      }
    },
    eraseText() {
      if (this.charIndex > 0) {
        if (!this.typeStatus) this.typeStatus = true;
        this.typeValue = this.displayTextArray[this.displayTextArrayIndex].substring(
          0,
          this.charIndex - 1
        );
        this.charIndex -= 1;
        setTimeout(this.eraseText, this.erasingSpeed);
      } else {
        this.typeStatus = false;
        this.displayTextArrayIndex += 1;
        if (this.displayTextArrayIndex >= this.displayTextArray.length)
          this.displayTextArrayIndex = 0;
        setTimeout(this.typeText, this.typingSpeed + 1000);
      }
    },
  },
};
</script>
